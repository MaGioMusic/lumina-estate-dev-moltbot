// Helper function to get local image path
export function getLocalImagePath(externalUrl: string): string {
  const localPath = imageMap[externalUrl as keyof typeof imageMap];
  
  if (!localPath) {
    console.warn(`No local mapping found for external URL: ${externalUrl}`);
    // Return a placeholder image for missing mappings
    return '/images/placeholder.jpg';
  }
  
  return localPath;
}

// Helper function to validate image URLs
export function isExternalUrl(url: string): boolean {
  return url.startsWith('http://') || url.startsWith('https://');
}

// Helper function to replace external URLs in components
export function replaceExternalImages(content: string): string {
  let updatedContent = content;
  
  Object.entries(imageMap).forEach(([externalUrl, localPath]) => {
    updatedContent = updatedContent.replace(new RegExp(externalUrl.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g'), localPath);
  });
  
  return updatedContent;
} 